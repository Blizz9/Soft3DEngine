namespace Soft3DEngine
{
    public struct UnityVector4
    {
        public float X;
        public float Y;
        public float Z;
        public float W;

        public UnityVector4(float x, float y, float z, float w)
        {
            X = x;
            Y = y;
            Z = z;
            W = w;
        }
    }
}
